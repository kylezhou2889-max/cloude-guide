export function useLanguage(): [Lang, (l: Lang) => void] {
  const stored = (localStorage.getItem('lang') as Lang) || 'zh';
  const set = (l: Lang) => {
    localStorage.setItem('lang', l);
    window.dispatchEvent(new CustomEvent('langchange', { detail: l }));
  };
  return [stored, set];
}
